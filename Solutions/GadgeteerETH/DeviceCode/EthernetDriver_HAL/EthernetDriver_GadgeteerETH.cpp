#include <tinyhal.h>
#include <STM32F2_ETH_lwip_adapter.h>

BOOL Network_Interface_Bind(int index)
{
    return STM32F2_ETH_LWIP_Driver::Bind();
}

int  Network_Interface_Open(int index)
{
    return STM32F2_ETH_LWIP_Driver::Open(index);
}

BOOL Network_Interface_Close(int index)
{
    return STM32F2_ETH_LWIP_Driver::Close();
}
